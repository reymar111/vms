<script>
import { useForm, Link, Head } from '@inertiajs/vue3';
import AuthenticatedLayout from '@/Layouts/AuthenticatedLayout.vue';

export default {
    props: ['violations', 'penalty_actions', 'statuses'],
    components: {
        AuthenticatedLayout,
        Link,
        Head
    },
    data() {
        return {
            form: useForm({
                id: '',
                transaction_number: '',
                name: '',
                status_id: '',
                penalty_action_id: '',
                resolution: '',
                is_escalate: false,
                active: false,
            }),

            loading_button: false,
            disabled_button: false,
            is_created: false,
            is_updated: false,
            is_deleted: false,

            search: '',


        }
    },
    mounted() {

    },
    computed: {
        filteredViolations() {
            if (!this.search) return this.violations;

            return this.violations.filter(violation => {
                return Object.values(violation).some(value =>
                    String(value).toLowerCase().includes(this.search.toLowerCase())
                );
            });
        }
    },
    methods: {
        openResolveForm(item) {
            this.form.id = item.id
            this.form.transaction_number = item.transaction_number
            this.form.name = item.full_name
            this.form.status_id = item.status_id
            this.form.penalty_action_id = item.penalty_action_id
            this.form.resolution = item.resolution
            this.form.active = true
        },

        escalate(item) {
            this.form.id = item.id
            this.form.transaction_number = item.transaction_number
            this.form.name = item.full_name
            this.form.status_id = 5
            this.form.penalty_action_id = item.penalty_action_id
            this.form.resolution = item.resolution
            this.form.active = true
            this.form.is_escalate = true
        },

        closeResolveForm() {
            this.form.id = ''
            this.form.transaction_number = ''
            this.form.name = ''
            this.form.active = false
            this.form.is_escalate = false
        },

        resolve() {
            this.form.patch('/transaction_violation/resolve/'+this.form.id, {
                onSuccess: () => {
                    this.closeResolveForm()
                    this.is_updated = true
                },
                onError: () => {

                }
            })
        },
    }
}
</script>
